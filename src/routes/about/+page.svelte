<h1>About</h1>

<p>
    Because of recent developments in the art community, some artists are
    leaving platforms like artstation. They opt to host art on their websites.
    It's the right thing to do for more control over their work.
</p>

<p>
    The problem is that it would be harder for people to discover them. This
    website tries to solve this. It's an alternative way to discover portfolios
    of artists. If you would like your portfolio to be listed, please email
    <code> xanderjakeq@humanart.work </code> with the subject `Add Folio`.
</p>

<p>
    Include the link, your alias, full name(optional) and at most 3 relevant tags.
</p>

<h1>AI?</h1>

<p>
    We will not host art, only links to your portfolio sites. The featured image
    of your website will be visible. So that people can see what your work is like
    ofc. These images have a risk of being scraped. You could change it to a
    lower resolution or add a watermark, it's up to you.
</p>

<p>
    Only human work will be supported. Websites will be reviewed before adding.
    If you see AI art on any of the portfolios, let us know so we can remove it.
</p>

<h1>open source</h1>

<p>
    This website is not built for profit. We will find ways for this website to
    be self-sustaining. The source code is on
    <a href="https://github.com/xanderjakeq/humanart.work">github</a>. Check it
    out, use it to create a clone of humanart.work or suggest changes by
    submitting an issue.
</p>
